import type { ArchLensConfig } from "../../schemas/config.js";
import type { GeneratedFile } from "./claude-skill.js";

/**
 * Generate the GitHub Action workflow for automated ArchLens sync.
 */
export function githubActionTemplate(config: ArchLensConfig): GeneratedFile {
  const content = `name: ArchLens Sync

on:
  # Run on push to main branch
  push:
    branches: [main]
    paths-ignore:
      - '**.md'
      - '.archlens/arc42/**'

  # Run weekly
  schedule:
    - cron: '0 9 * * 1'  # Monday 9am UTC

  # Manual trigger
  workflow_dispatch:

permissions:
  contents: write
  pull-requests: write

jobs:
  sync:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4
        with:
          fetch-depth: 0  # Full history for git diff analysis

      - name: Setup Node.js
        uses: actions/setup-node@v4
        with:
          node-version: '20'

      - name: Setup pnpm
        uses: pnpm/action-setup@v4
        with:
          version: 9

      - name: Install dependencies
        run: pnpm install --frozen-lockfile

      - name: Check for ArchLens
        id: check
        run: |
          if [ -f ".archlens/config.yaml" ]; then
            echo "initialized=true" >> $GITHUB_OUTPUT
          else
            echo "initialized=false" >> $GITHUB_OUTPUT
            echo "::warning::ArchLens is not initialized. Run archlens_init_project first."
          fi

      - name: Run ArchLens sync
        if: steps.check.outputs.initialized == 'true'
        id: sync
        run: npx archlens sync --json | tee archlens-sync-result.json

      - name: Check for drift errors
        if: steps.check.outputs.initialized == 'true'
        run: |
          npx archlens drift --json | tee archlens-drift-result.json
          ERRORS=$(node -e "try { const d=JSON.parse(require('fs').readFileSync('archlens-drift-result.json','utf8')); console.log(d.drift.filter(e=>e.severity==='error').length); } catch { console.log(0); }")
          if [ "$ERRORS" -gt 0 ]; then
            echo "::error::ArchLens detected $ERRORS drift error(s) that would block phase completion."
            exit 1
          fi

      - name: Create sync PR
        if: steps.check.outputs.initialized == 'true'
        uses: peter-evans/create-pull-request@v6
        with:
          commit-message: 'chore: archlens sync - update arc42 documentation'
          title: 'ArchLens Sync: Arc42 Documentation Updates'
          body: |
            ## ArchLens Sync

            Automated architecture sync detected changes that may require documentation updates.

            **Review the proposed changes to \`.archlens/arc42/\` files.**

            ---
            *Generated by [ArchLens](https://github.com/antigravity/project-archlens) sync loop*
          branch: archlens/sync
          delete-branch: true
          labels: archlens,documentation
`;

  return {
    relativePath: ".github/workflows/archlens-sync.yml",
    content,
  };
}
